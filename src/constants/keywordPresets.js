export function suggestRelated(selected = []) {
  const sel = new Set(selected);
  const out = new Map(); // 키워드 → 출현 횟수 (정렬 가중치)
  for (const k of selected) {
    const list = RELATED_KEYWORDS[k] || [];
    for (const r of list) if (!sel.has(r)) out.set(r, (out.get(r) || 0) + 1);
  }
  return [...out.entries()].sort((a, b) => b[1] - a[1]).map(([k]) => k);
}

