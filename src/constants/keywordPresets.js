// 카테고리/키워드 유틸 — UI 가 직접 import 해서 재사용
export function listCategories(orgId = 'moj') {
  return ORG_PRESETS[orgId]?.categories || [];
}
export function flattenAllKeywords(orgId = 'moj') {
  const cats = listCategories(orgId);
  const all = [];
  for (const c of cats) all.push(...(c.core || []), ...(c.extended || []));
  return [...new Set(all)];
}

