export const MEDIA_TYPES = ['중앙언론', '방송사', '정부/공공기관', '지방언론', '인터넷언론', '기타'];

/**
 * source 문자열을 언론 유형으로 분류한다.
 * - 부분 문자열 매칭 (양방향)
 * - 매칭 우선순위: 정부/공공기관 → 방송사 → 중앙 → 지방 → 인터넷
 */
export function classifyMedia(source = '') {
  const s = String(source).trim();
  if (!s) return '기타';
  const order = ['정부/공공기관', '방송사', '중앙언론', '지방언론', '인터넷언론'];
  for (const tier of order) {
    const list = MEDIA_BY_TYPE[tier];
    if (list.some(m => s.includes(m) || m.includes(s))) return tier;
  }
  return '기타';
}

/**
 * 기사 배열에서 언론 유형별 건수를 집계한다.
 */
export function countByMediaType(articles = []) {
  const out = Object.fromEntries(MEDIA_TYPES.map(t => [t, 0]));
  for (const a of articles) {
    const t = a.mediaType || classifyMedia(a.source);
    out[t] = (out[t] || 0) + 1;
  }
  return out;
}
