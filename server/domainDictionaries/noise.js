/**
 * 모든 노이즈 어휘를 평탄화. category 미지정 시 전 카테고리 사용.
 */
export function getNoiseTerms(categories = null) {
  const cats = categories || Object.keys(NOISE_BY_CATEGORY);
  const out = [];
  for (const c of cats) {
    if (NOISE_BY_CATEGORY[c]) out.push(...NOISE_BY_CATEGORY[c]);
  }
  return [...new Set(out)];
}

export function getStrongNoiseTerms() {
  return [...STRONG_NOISE_TERMS];
}

/**
 * 동음이의어 검사 — 텍스트에 ambiguous 단어가 있는데 needsContext 단어가 없으면
 * 해당 단어는 도메인 무관으로 간주 (감점 사유 반환).
 */
export function checkAmbiguous(text = '') {
  const reasons = [];
  for (const [word, cfg] of Object.entries(AMBIGUOUS_TERMS)) {
    if (!text.includes(word)) continue;
    const hasContext = (cfg.needsContext || []).some(c => text.includes(c));
    if (!hasContext) reasons.push({ word, needsContext: cfg.needsContext });
  }
  return reasons;
}
