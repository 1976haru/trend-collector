/**
 * 카테고리별 사전 + 공통 사전을 합친 단어 목록.
 * @param {string} category — 'moj' / 'probation' / 'corrections' / 'immigration' / 'prosecution' / 'court' / 'juvenile' / 'general'
 */
export function getDomainTerms(category = 'general') {
  const cat = CATEGORY_TERMS[category] || [];
  // 중복 제거 + 순서 유지
  return [...new Set([...COMMON_JUSTICE, ...cat])];
}

/**
 * 강한 신호 단어 — 도메인 맥락 통과 가산점 큰 단어.
 */
export function getStrongDomainSignals(category = 'general') {
  const cat = CATEGORY_TERMS[category] || [];
  // 카테고리별 사전 중 STRONG_DOMAIN_SIGNALS 과 교집합 + 공용 강신호
  const strong = new Set(STRONG_DOMAIN_SIGNALS);
  for (const t of cat) {
    // 길이 5+ 의 고유명사 (보호관찰소 / 출입국외국인정책본부 등) 도 강신호
    if (t.length >= 5 && !/[\s]/.test(t)) strong.add(t);
  }
  return [...strong];
}
